function multiplyTwoNumbers(firstNumber, secondNumber) {
  return firstNumber * secondNumber;
}

function calculateSquare(squareSide) {
  return multiplyTwoNumbers(squareSide, squareSide);
}

function calculateRectangleSquare(firstRectangleSide, secondRectangleSide) {
  return multiplyTwoNumbers(firstRectangleSide, secondRectangleSide);
}

function calculateParallelogramSquare(parellelogramSide, parallelogramHeight) {
  return multiplyTwoNumbers(parellelogramSide, parallelogramHeight);
}

function calculateLozengeSquare(lozengeSide, lozengeHeight) {
  return multiplyTwoNumbers(lozengeSide, lozengeHeight);
}
